import { Component } from '@angular/core';
import { Meta, Title } from '@angular/platform-browser';
import { ArticlesService } from '../../../../../servises/articles.service';

@Component({
  selector: 'app-home-en-blog-seventy-two',
  templateUrl: './home-en-blog-seventy-two.component.html',
  styleUrl: './home-en-blog-seventy-two.component.scss',
})
export class HomeEnBlogSeventyTwoComponent {
  constructor(
    private meta: Meta,
    private titleService: Title,
    private articleServ: ArticlesService
  ) {}
  ngOnInit(): void {
    this.titleService.setTitle('What is an Economic Calendar - Arapov.trade');
    this.meta.updateTag({ name: 'robots', content: 'index, follow' });
    this.meta.addTag({
      name: 'description',
      content:
        'A complete guide to the stock market economic calendar. How to use it to predict market movements, which events affect stocks and indices, and trading strategies based on news.',
    });
    this.meta.updateTag({ name: 'author', content: 'Igor Arapov' });
    this.meta.updateTag({ name: 'datePublished', content: '2025-02-15' });
    this.meta.updateTag({
      property: 'og:image',
      content: '/assets/img/content/economiccalendar.png',
    });

    this.gerRandom();
  }
  randomArticleRus: any = [];
  gerRandom() {
    this.randomArticleRus = this.articleServ.getRandomUkArticles();
  }
}
