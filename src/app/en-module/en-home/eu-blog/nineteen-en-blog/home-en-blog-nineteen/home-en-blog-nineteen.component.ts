import { Component, OnInit } from '@angular/core';
import { Meta, Title } from '@angular/platform-browser';
import { ArticlesService } from '../../../../../servises/articles.service';

@Component({
  selector: 'app-home-en-blog-nineteen',
  templateUrl: './home-en-blog-nineteen.component.html',
  styleUrl: './home-en-blog-nineteen.component.scss',
})
export class HomeEnBlogNineteenComponent implements OnInit {
  constructor(
    private meta: Meta,
    private titleService: Title,
    private articleServ: ArticlesService
  ) {}
  ngOnInit(): void {
    this.titleService.setTitle('The Wyckoff Method in Trading - Arapov.trade');
    this.meta.updateTag({ name: 'robots', content: 'index, follow' });
    this.meta.addTag({
      name: 'description',
      content:
        'The Wyckoff Method is a powerful market analysis tool based on the laws of supply and demand. Learn how to apply the Wyckoff Method to identify trends, entry and exit points in the market.',
    });
    this.gerRandom();
  }
  randomArticleRus: any = [];
  gerRandom() {
    this.randomArticleRus = this.articleServ.getRandomUkArticles();
  }
}
